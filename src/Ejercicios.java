public class Ejercicios {
    public static void main(String[] args) {
        int temperaturaC = 30;
        double fahrenheit = (temperaturaC * 1.8) + 32;
        System.out.println(fahrenheit);

        int temperaturaF = 86;
        double centigrados = (temperaturaF - 32) * 0.55;
        System.out.println(centigrados);
    }
}
